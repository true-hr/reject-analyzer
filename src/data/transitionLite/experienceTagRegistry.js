export const EXPERIENCE_TAG_BY_ID = Object.freeze(
  Object.fromEntries(EXPERIENCE_TAG_REGISTRY.map((tag) => [tag.id, tag]))
);

export function getExperienceTagById(id) {
  if (!id || typeof id !== "string") return null;
  return EXPERIENCE_TAG_BY_ID[id.trim()] ?? null;
}

export function getExperienceTagsByIds(ids) {
  if (!Array.isArray(ids)) return [];
  return ids
    .map((id) => getExperienceTagById(id))
    .filter(Boolean);
}
