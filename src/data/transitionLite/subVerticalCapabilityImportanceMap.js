export function getSubVerticalCapabilityImportanceReason(subVertical, capabilityId, axisKey = "axis4") {
  const normalizedSubVertical = String(subVertical || "").trim();
  const normalizedCapabilityId = String(capabilityId || "").trim();
  const normalizedAxisKey = String(axisKey || "").trim();
  if (!normalizedSubVertical || !normalizedCapabilityId || !normalizedAxisKey) return "";
  return SUB_VERTICAL_CAPABILITY_IMPORTANCE_MAP[normalizedSubVertical]?.[normalizedAxisKey]?.[normalizedCapabilityId] || "";
}
